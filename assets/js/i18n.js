const I18N_KEY = "qazrentast_lang";
const DEFAULT_LANG = "ru";

function getLang(){ return localStorage.getItem(I18N_KEY) || DEFAULT_LANG; }
function setLang(lang){ localStorage.setItem(I18N_KEY, lang); }

function applyI18n(){
  const lang = getLang();
  const t = window.dict[lang] || window.dict[DEFAULT_LANG];
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach(el=>{
    const k = el.getAttribute("data-i18n");
    if(t[k]) el.textContent = t[k];
  });
  document.querySelectorAll("[data-i18n-ph]").forEach(el=>{
    const k = el.getAttribute("data-i18n-ph");
    if(t[k]) el.setAttribute("placeholder", t[k]);
  });
}

document.addEventListener("DOMContentLoaded", ()=>{
  const sel = document.querySelector("#langSelect");
  if(sel){
    sel.value = getLang();
    sel.addEventListener("change", ()=>{ setLang(sel.value); applyI18n(); });
  }
  applyI18n();
});
